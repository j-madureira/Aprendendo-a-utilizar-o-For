/*Criar uma variável numero que tem um tipo de dado Number, esse Number é 12
  Criar um for para se repetir, a quantidade de repetição precisa ser o numero
  Que está setado na variável numero
*/ 

var numero = 12;
for(var nu = 0; nu < numero; ++nu){
    console.log(`Este código esta sendo executado pela ${nu} vez.`);
}